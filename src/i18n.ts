export type TranslationKey = keyof typeof TRANSLATIONS.en;

let _locale: Locale = (localStorage.getItem('jh-locale') as Locale | null) ?? 'en';

export function getLocale(): Locale { return _locale; }

export function setLocale(locale: Locale): void {
  _locale = locale;
  localStorage.setItem('jh-locale', locale);
  document.documentElement.lang = locale;
}

export function t(key: TranslationKey): string {
  return (TRANSLATIONS[_locale] as Record<string, string>)[key];
}
